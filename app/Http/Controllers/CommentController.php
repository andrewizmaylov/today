     */
    public function store(Request $request)
    {   
        // dd($request);
        $data = $request->validate(['msg' => 'required', 'date' => 'required']);
        if($data) {
            auth()->user()->comment()->create($data);
            return response(['message' => 'Entry was created'], 201);
        }
    }

